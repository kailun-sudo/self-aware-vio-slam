def load_sequence_data(seq_dir: str | Path) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Load SLAM metrics and pose errors for a sequence or run directory."""
    seq_dir = Path(seq_dir)
    metrics = pd.read_csv(seq_dir / 'slam_metrics.csv')
    errors = pd.read_csv(seq_dir / 'pose_errors.csv')
    return metrics, errors


def _align_metrics_and_errors(metrics: pd.DataFrame, errors: pd.DataFrame) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Trim metrics/errors to the same row count when packaging differs by one row."""
    aligned_length = min(len(metrics), len(errors))
    if aligned_length <= 0:
        raise ValueError("Cannot align empty metrics/errors tables.")
    if len(metrics) != len(errors):
        print(f"  Note: aligning metrics/errors row mismatch ({len(metrics)} vs {len(errors)}) -> {aligned_length}")
    return (
        metrics.iloc[:aligned_length].reset_index(drop=True),
        errors.iloc[:aligned_length].reset_index(drop=True),
    )


